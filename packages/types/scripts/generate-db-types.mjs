/**
 * PodMind — Database type generator.
 *
 * Introspects a PostgreSQL database (the docs/database schema) and emits
 * `src/database.ts`: one union type per enum and Row/Insert types per table.
 *
 * Run:  DATABASE_URL=postgres://... node scripts/generate-db-types.mjs
 * CI regenerates and fails the build if the committed file drifts.
 */

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import pg from "pg";

const DATABASE_URL =
  process.env.DATABASE_URL ?? "postgres://postgres:localtest@localhost:5432/docs_schema_test";

const TYPE_MAP = {
  uuid: "string",
  text: "string",
  citext: "string",
  varchar: "string",
  "character varying": "string",
  char: "string",
  inet: "string",
  int2: "number",
  int4: "number",
  int8: "number",
  smallint: "number",
  integer: "number",
  bigint: "number",
  numeric: "number",
  real: "number",
  "double precision": "number",
  float4: "number",
  float8: "number",
  bool: "boolean",
  boolean: "boolean",
  json: "Json",
  jsonb: "Json",
  timestamptz: "string",
  "timestamp with time zone": "string",
  timestamp: "string",
  date: "string",
  time: "string",
  interval: "string",
  bytea: "string",
  tsvector: "string",
  vector: "number[]",
  hstore: "Record<string, string>",
  ltree: "string",
};

/** snake_case → PascalCase */
const pascal = (s) => s.replace(/(^|_)([a-z0-9])/g, (_, __, c) => c.toUpperCase());

const client = new pg.Client({ connectionString: DATABASE_URL });
await client.connect();

// ---------------------------------------------------------------- enums
const { rows: enumRows } = await client.query(`
  SELECT t.typname AS name,
         array_agg(e.enumlabel ORDER BY e.enumsortorder) AS labels
    FROM pg_type t
    JOIN pg_enum e       ON e.enumtypid = t.oid
    JOIN pg_namespace n  ON n.oid = t.typnamespace
   WHERE n.nspname = 'public'
   GROUP BY t.typname
   ORDER BY t.typname
`);

// --------------------------------------------------------------- columns
const { rows: colRows } = await client.query(`
  SELECT c.table_name,
         c.column_name,
         c.udt_name,
         c.data_type,
         c.is_nullable = 'YES'                                  AS nullable,
         (c.column_default IS NOT NULL OR c.is_identity = 'YES') AS has_default,
         c.data_type = 'ARRAY'                                  AS is_array,
         c.is_generated = 'ALWAYS'                              AS is_generated
    FROM information_schema.columns c
    JOIN information_schema.tables t
      ON t.table_schema = c.table_schema AND t.table_name = c.table_name
   WHERE c.table_schema = 'public'
     AND t.table_type = 'BASE TABLE'
   ORDER BY c.table_name, c.ordinal_position
`);
await client.end();

const enumNames = new Set(enumRows.map((e) => e.name));

function tsType(col) {
  // ARRAY columns report udt_name like "_text"
  const udt = col.is_array ? col.udt_name.replace(/^_/, "") : col.udt_name;
  let base;
  if (enumNames.has(udt)) base = pascal(udt);
  else base = TYPE_MAP[udt] ?? TYPE_MAP[col.data_type] ?? "unknown";
  if (col.is_array) base = `${base}[]`;
  return base;
}

const lines = [];
lines.push("/**");
lines.push(" * AUTO-GENERATED by scripts/generate-db-types.mjs — DO NOT EDIT BY HAND.");
lines.push(" * Source of truth: docs/database/*.sql (live Supabase project).");
lines.push(` * Generated: ${new Date().toISOString()}`);
lines.push(" */");
lines.push("");
lines.push("export type Json = string | number | boolean | null | { [key: string]: Json } | Json[];");
lines.push("");

const parseLabels = (v) => Array.isArray(v) ? v : String(v).replace(/^{|}$/g, "").split(",").map((x) => x.replace(/^"|"$/g, ""));
for (const e of enumRows) {
  e.labels = parseLabels(e.labels);
  lines.push(`export type ${pascal(e.name)} = ${e.labels.map((l) => JSON.stringify(l)).join(" | ")};`);
}
lines.push("");

const tables = new Map();
for (const col of colRows) {
  if (!tables.has(col.table_name)) tables.set(col.table_name, []);
  tables.get(col.table_name).push(col);
}

for (const [table, cols] of tables) {
  const name = pascal(table);
  lines.push(`export interface ${name} {`);
  for (const c of cols) {
    lines.push(`  ${c.column_name}: ${tsType(c)}${c.nullable ? " | null" : ""};`);
  }
  lines.push("}");
  lines.push("");
  lines.push(`export interface ${name}Insert {`);
  for (const c of cols) {
    if (c.is_generated) continue; // generated columns are never written
    const optional = c.nullable || c.has_default;
    lines.push(
      `  ${c.column_name}${optional ? "?" : ""}: ${tsType(c)}${c.nullable ? " | null" : ""};`,
    );
  }
  lines.push("}");
  lines.push("");
}

// Convenience registry: table name -> Row type
lines.push("export interface Tables {");
for (const table of tables.keys()) {
  lines.push(`  ${table}: ${pascal(table)};`);
}
lines.push("}");
lines.push("");
lines.push(`export const TABLE_NAMES = [${[...tables.keys()].map((t) => JSON.stringify(t)).join(", ")}] as const;`);
lines.push("export type TableName = (typeof TABLE_NAMES)[number];");
lines.push("");

const out = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "database.ts");
writeFileSync(out, lines.join("\n"));
console.log(
  `generated ${enumRows.length} enums, ${tables.size} tables -> src/database.ts (${lines.length} lines)`,
);
